public class EmployeeInfo {
    
    int id;
    int emp_id;
    int emp_address_id;
    int emp_attend_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

     
    public int getEmp_id() {
        return emp_id;
    }

    public void setEmp_id(int emp_id) {
        this.emp_id = emp_id;
    }

    public int getEmp_address_id() {
        return emp_address_id;
    }

    public void setEmp_address_id(int emp_address_id) {
        this.emp_address_id = emp_address_id;
    }

    public int getEmp_attend_id() {
        return emp_attend_id;
    }

    public void setEmp_attend_id(int emp_attend_id) {
        this.emp_attend_id = emp_attend_id;
    }
    
    
    
}
